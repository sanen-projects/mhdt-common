package com.mhdt.log;

import com.mhdt.log.Log.Level;

/**
 * 
 * @author LazyToShow
 * Date: 2018/06/12
 * Time: 09:17
 */
public class LogManager {

	static Level level = Level.INFO;

	/**
	 * 
	 * @param level
	 */
	public static void SetGlobalLevel(Level level) {
		LogManager.level = level;

	}
	
	/**
	 * 
	 * @return
	 */
	public static Level getGlobalLevel() {
		return level;
		
	}
	
	static boolean debugEnable = false;
	
	public static void setDebugEnable(boolean flag) {
		debugEnable  = flag;
	}
	
	public static boolean isDebugEnable() {
		return debugEnable;
	}

}
